from typing import List

class Solution:
	def splitArray(self, nums: List[int], m: int) -> int:
		ans = sum(nums)
		l, r = 0, ans
		while l <= r:
			mid = (l + r) // 2
			max_water = water = cnt = 0
			for i in nums:
				water += i 
				if water > mid:
					max_water = max(max_water, water - i)
					water = i 
					cnt += 1
			max_water = max(max_water, water)
			if cnt + 1 > m:
				l = mid + 1
			else:
				r = mid - 1
				ans = min(ans, max_water)			
		return ans		

